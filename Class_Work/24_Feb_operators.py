# PYTHON OPERATORS
# Operators are used to perform operations on variables and values.

a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Comparison Operators
print("a > b:", a > b)
print("a == b:", a == b)

# Logical Operators
print("True and False:", True and False)